package bookcheck;

public class BookCheck {
	private int logs;
	private int bookInfo;
	private String mId;
	private String bookCate;
	private String bookName;
	private String bookOutDate;
	private String bookLimitDate;
	private String bookInDate;
	
	public int getLogs() {
		return logs;
	}
	public void setLogs(int logs) {
		this.logs = logs;
	}
	public int getBookInfo() {
		return bookInfo;
	}
	public void setBookInfo(int bookInfo) {
		this.bookInfo = bookInfo;
	}
	public String getmId() {
		return mId;
	}
	public void setmId(String mId) {
		this.mId = mId;
	}
	public String getBookCate() {
		return bookCate;
	}
	public void setBookCate(String bookCate) {
		this.bookCate = bookCate;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getBookOutDate() {
		return bookOutDate;
	}
	public void setBookOutDate(String bookOutDate) {
		this.bookOutDate = bookOutDate;
	}
	public String getBookLimitDate() {
		return bookLimitDate;
	}
	public void setBookLimitDate(String bookLimitDate) {
		this.bookLimitDate = bookLimitDate;
	}
	public String getBookInDate() {
		return bookInDate;
	}
	public void setBookInDate(String bookInDate) {
		this.bookInDate = bookInDate;
	}
}
